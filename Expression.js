let result = 5 + 10;  // 5 + 10 یک expression است که نتیجه آن 15 می‌شود
console.log(result);  // چاپ می‌کند 15


let isEqual = (x === y);  // x === y یک expression است که نتیجه آن true یا false است
console.log(isEqual);

let square = (a) => a * a;  // این یک expression است که تابعی ایجاد می‌کند
console.log(square(4));  // چاپ می‌کند 16

let isAdult = (age >= 18 && age <= 65);  // یک expression که نتیجه‌ی آن true یا false است
console.log(isAdult);

let max = (a > b) ? a : b;  // این یک expression است که مقدار max را به‌طور شرطی انتخاب می‌کند
console.log(max);  // چاپ می‌کند بزرگترین عدد بین a و b

